// Define the function city_country
function City_name(city, country) {
    return "".concat(city, ", is in ").concat(country);
}
// Call the function with at least three city-country pairs
var city1 = City_name("Lahore", "Pakistan");
var city2 = City_name("New York", "USA");
var city3 = City_name("Pairs", "France");
// Print the values returned by the function
console.log(city1);
console.log(city2);
console.log(city3);
